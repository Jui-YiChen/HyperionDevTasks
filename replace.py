sentence = "The!quick!brown!fox!jumps!over!the!lazy!dog."

replacesentense = sentence.replace("!"," ") #replace ! with " "
print(replacesentense)

cap_replacesentense = replacesentense.upper() #change to upper case
print(cap_replacesentense)

reverse_cap_replacesentense = cap_replacesentense[::-1] #reverse string
print(reverse_cap_replacesentense)
